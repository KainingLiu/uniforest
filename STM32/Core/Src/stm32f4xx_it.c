/**
 * @brief  USART2 IRQ — JY61P IMU (PD5=TX, PD6=RX, AF7)
 */
void USART2_IRQHandler(void)
{
    extern UART_HandleTypeDef huart2;
    if (__HAL_UART_GET_FLAG(&huart2, UART_FLAG_RXNE))
    {
        uint8_t byte = (uint8_t)(huart2.Instance->DR & 0xFF);
        void JY61P_ParseByte(uint8_t byte);
        JY61P_ParseByte(byte);
    }
}

/**
 * @brief  USART1 IRQ — SBUS (DBUS) receiver (PB7=RX, AF7)
 * @note   100k bps, 8E2. Handles RXNE for data and ORE/FE for error recovery.
 */
void USART1_IRQHandler(void)
{
    extern UART_HandleTypeDef huart1;
    uint32_t sr = huart1.Instance->SR;

    /* Error recovery: overrun or framing error → discard byte and reset parser */
    if (sr & (USART_SR_ORE | USART_SR_FE))
    {
        (void)huart1.Instance->DR;   /* read DR to clear ORE/FE flags */
        void SBUS_ResetParser(void);
        SBUS_ResetParser();
        return;
    }

    /* Data byte received */
    if (__HAL_UART_GET_FLAG(&huart1, UART_FLAG_RXNE))
    {
        uint8_t byte = (uint8_t)(huart1.Instance->DR & 0xFF);
        void SBUS_ParseByte(uint8_t byte);
        SBUS_ParseByte(byte);
    }
}

